package Sorting;

public class returnValueMessage {
    private final Integer value;

    /**
     * Creates a returnValueMessage object
     * @param n the value that will be sent
     */
    public returnValueMessage(int n){
        value = n;
    }

    /**
     * getter for value
     * @return value
     */
    public int getValue() {
        return value;
    }
}

